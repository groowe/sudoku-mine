def preprint(line):
    s = str(line)
    s = s.replace("None"," ").replace("[","|")
    s = s.replace("]","|")
    line = s
    newline = ""
    countchar = 0
    checkchar = ","
    for char in line:
        if char == checkchar:
            countchar +=1
            if countchar % 3 == 0:
                newline +='|'
            else:
                newline +=' '
        else:
            newline+=char
    return newline


